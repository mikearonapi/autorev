async function populateMaintenanceSpecs() {
  console.log('Starting maintenance specs population...');
  console.log(`Processing ${maintenanceSpecs.length} vehicles\n`);

  let successCount = 0;
  let errorCount = 0;

  for (const spec of maintenanceSpecs) {
    try {
      // Add timestamps
      spec.verified_at = new Date().toISOString();

      const { data, error } = await supabase
        .from('vehicle_maintenance_specs')
        .upsert(spec, { onConflict: 'car_slug' })
        .select();

      if (error) {
        console.error(`❌ Error inserting ${spec.car_slug}:`, error.message);
        errorCount++;
      } else {
        console.log(`✅ Inserted maintenance specs for: ${spec.car_slug}`);
        successCount++;
      }
    } catch (err) {
      console.error(`❌ Exception for ${spec.car_slug}:`, err.message);
      errorCount++;
    }
  }

  console.log(`\n${'='.repeat(50)}`);
  console.log(`Complete! Success: ${successCount}, Errors: ${errorCount}`);
  console.log(`${'='.repeat(50)}`);
}

// Run the script
populateMaintenanceSpecs()
  .then(() => process.exit(0))
  .catch(err => {
    console.error('Fatal error:', err);
    process.exit(1);
  });

